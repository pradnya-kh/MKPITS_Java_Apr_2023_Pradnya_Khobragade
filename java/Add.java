
public class Add {
	public static int add(int a, int b) {
		return a+b;
	}
	public static long add(long a, long b) {
		return a+b;
	}
	public static short add(short a, short b) {
		return (short)(a+b);
	}
	public static double add(double a, double b) {
		return a+b;
	}
	public static float add(float a, float b) {
		return a+b;
	}
}